#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d;

	d = dst;
	if (NULL == src && NULL == dst)
		return (NULL);
	while (n--)
		*d++ = *(unsigned char *)src++;
	return (dst);
}
